     private void populateComboBoxes() {
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();

        Connection conn = getConnection();

        // SAFETY CHECK: If connection fails, stop here and don't crash!
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Could not connect to the database to load dropdowns.");
            return;
        }

        // Safely run the queries
        try (Statement stmt = conn.createStatement()) {

            ResultSet rsStudent = stmt.executeQuery("SELECT student_id, first_name, last_name FROM student");
            while (rsStudent.next()) {
                jComboBox1.addItem(rsStudent.getInt("student_id") + " - " + rsStudent.getString("first_name") + " " + rsStudent.getString("last_name"));
            }

            ResultSet rsCourse = stmt.executeQuery("SELECT course_id, course_name FROM course");
            while (rsCourse.next()) {
                jComboBox2.addItem(rsCourse.getInt("course_id") + " - " + rsCourse.getString("course_name"));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "SQL Error: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
