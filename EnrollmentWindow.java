    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
          int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an enrollment record to delete from the table.");
            return;
        }

// Get the enrollment_id from the first column (index 0) of the selected row
        int enrollmentId = (int) jTable1.getValueAt(selectedRow, 0);

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to drop this student from the course?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = getConnection(); PreparedStatement pst = conn.prepareStatement("DELETE FROM enrolled_subject WHERE enrollment_id=?")) {

                pst.setInt(1, enrollmentId);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Enrollment Deleted Successfully!");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_DeleteActionPerformed

    private void RefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefreshActionPerformed
        // TODO add your handling code here:
        populateComboBoxes();
          DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        // grabs the IDs and translates them into actual Names!
        String query = "SELECT e.enrollment_id, s.first_name, s.last_name, c.course_name, e.enrollment_date " +
                       "FROM enrolled_subject e " +
                       "JOIN student s ON e.student_id = s.student_id " +
                       "JOIN course c ON e.course_id = c.course_id " + 
                       "ORDER BY e.enrollment_id ASC";
        // Connect and fetch the data
        try (Connection conn = getConnection()) {
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Cannot connect to Database!");
                return;
            }

            try (Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(query)) {

                // Loop through every saved enrollment and add it to the table
                while (rs.next()) {
                    int enrollId = rs.getInt("enrollment_id");
                    String studentName = rs.getString("first_name") + " " + rs.getString("last_name");
                    String courseName = rs.getString("course_name");
                    java.sql.Date enrollDate = rs.getDate("enrollment_date");

                    // Add the row to your jTable1
                    model.addRow(new Object[]{enrollId, studentName, courseName, enrollDate});
                }
            }
            
            
            if (evt != null) {
                JOptionPane.showMessageDialog(this, "Course List Refreshed!");
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error loading table: " + ex.getMessage());
            ex.printStackTrace(); // Prints the exact SQL error in the output box
        }
    }//GEN-LAST:event_RefreshActionPerformed

